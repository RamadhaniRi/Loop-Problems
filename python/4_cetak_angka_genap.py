# Pakai For loop
print("==== For Loop ====")

for i in range(2, 21, 2): #kita akan mencetak angka genap dari 2 sampai 20, kita pakai step 2 agar +2
    print(i)

# Pakai While loop
print("==== While Loop ====")

i = 2

while i <= 20: #selama i kurang atau sama dengan 20
    print(i)
    i += 2 #i + 2 agar hasilnya genap